import React from "react";
import styled, { ThemeProvider } from "styled-components";
import Button from "./components/Button";

const AppBlock = styled.div`
  width: 512px;
  margin: 0 auto;
  margin-top: 4rem;
  border: 1px solid black;
  padding: 1rem;
`;

function App() {
  return (
    <ThemeProvider // styled-components로 만드는 모든 컴포넌트에서 조회하여 사용할 수 있는 전역적인 값 설정
      theme={{
        // theme 설정시 ThemeProvider 내부에 랜더링된 styled-components로 만든 컴포넌트에서 palette를 조회하여 사용할 수 있음
        palette: {
          blue: "#228be6",
          gray: "#495057",
          pink: "#f06595",
        },
      }}
    >
      <AppBlock>
        <Button>BUTTON</Button>
      </AppBlock>
    </ThemeProvider>
  );
}

export default App;

